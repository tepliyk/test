                soccer</a>)</b> – специально разработаны для уличных игр, <b>фристайла (</b><b>free style)</b>, имеют
        яркий дизайн, невысокую стоимость, покрытие из твердых материалов;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    </p>
    <p style="text-align: justify;">&nbsp; &nbsp; - облегченные, утяжеленные, &nbsp;со шнурком – специально разработаны
        для оттачивания навыков владения мячом.</p>
